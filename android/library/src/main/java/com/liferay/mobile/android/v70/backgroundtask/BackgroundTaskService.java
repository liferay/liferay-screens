package com.liferay.mobile.android.v70.backgroundtask;

import com.liferay.mobile.android.http.file.UploadData;
import com.liferay.mobile.android.service.BaseService;
import com.liferay.mobile.android.service.JSONObjectWrapper;
import com.liferay.mobile.android.service.Session;

import org.json.JSONArray;
import org.json.JSONException;
import org.json.JSONObject;

/**
 * @author Bruno Farache
 */
public class BackgroundTaskService extends BaseService {

	public BackgroundTaskService(Session session) {
		super(session);
	}

	public Integer getBackgroundTasksCount(long groupId, String taskExecutorClassName, String completed) throws Exception {
		JSONObject _command = new JSONObject();

		try {
			JSONObject _params = new JSONObject();

			_params.put("groupId", groupId);
			_params.put("taskExecutorClassName", checkNull(taskExecutorClassName));
			_params.put("completed", checkNull(completed));

			_command.put("/backgroundtask.backgroundtask/get-background-tasks-count", _params);
		}
		catch (JSONException _je) {
			throw new Exception(_je);
		}

		JSONArray _result = session.invoke(_command);

		if (_result == null) {
			return null;
		}

		return _result.getInt(0);
	}

	public String getBackgroundTaskStatusJson(long backgroundTaskId) throws Exception {
		JSONObject _command = new JSONObject();

		try {
			JSONObject _params = new JSONObject();

			_params.put("backgroundTaskId", backgroundTaskId);

			_command.put("/backgroundtask.backgroundtask/get-background-task-status-json", _params);
		}
		catch (JSONException _je) {
			throw new Exception(_je);
		}

		JSONArray _result = session.invoke(_command);

		if (_result == null) {
			return null;
		}

		return _result.getString(0);
	}

}